# for loops
import time

for i in range(10):
    print(i+1)
    
print()

for i in range(50, 100+1):
    print(i)
    
print()    
    
for i in range(10, 20+1, 2):
    print(i)

print()

for i in "Joca Martins":
    print(i)
    
print()

for seconds in range(10, 0, -1):
    print(seconds)
    time.sleep(1)
print("Happy New Year!")